//: # The Basic Composition of Knowledge Graph
//: There are many real-world scenarios that are well suited to be represented by knowledge graphs. For example, in a social network graph, we can have both "person" entities and "company" entities. The relationship between people can be "friends" or "colleagues". The relationship between a person and a company can be a "current" or "former" relationship.
//:
//: The basic composition of the knowledge graph is the triple composed of **"Entity-Relationship-Entity"**, which is the core of the knowledge graph.
//:
//: **Entities** refer to things in the real world such as people, place names, concepts, etc., while **relationship**s are used to describe some kind of connection between different entities. In a knowledge graph, two nodes can be connected together with an undirected edge if there is some kind of relationship between them. Here the nodes are entities and the edge between two nodes is called a relationship.
